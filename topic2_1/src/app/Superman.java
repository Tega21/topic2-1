package app;

/**
 * The Superman class is a subclass of SuperHero that represents the superhero Superman.
 */

public class Superman extends SuperHero{
	
	/**
     * Constructs a new Batman superhero with specified health.
     *
     * @param health The initial health value of Batman.
     */
    public Superman(int health){
        super("Superman", health);
    }
}
